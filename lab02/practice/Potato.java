package practice;

public class Potato {

    /* Instance Variables */
    private String variety;
    private int age;


    /* Constructors */
    public Potato() {
        this.variety = "Russet Burbank";
        this.age = 0;
    }

    public Potato(String variety, int age) {
        this.variety = variety;
        this.age = age;
    }


}
